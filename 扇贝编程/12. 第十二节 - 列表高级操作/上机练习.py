"""刚刚听班主任讲她看到几门考试的倒数三名里都有贾宝玉和秦可卿，我们来统计贾宝玉和秦可卿他们到底在几门课里是倒数前三呢？
要求：在屏幕上分别打印出：
贾宝玉排在倒数三名的次数是xxx
秦可卿排在倒数三名的次数是xxx
1️⃣ 首先，你需要定义一个函数 last_three(student_name)，这个函数接收一个学生的名字作为参数，然后统计该学生在所有科目中的倒数排名次数。你可以通过遍历每一门课的排名列表来实现这一点。
2️⃣ 在函数内部，你可以使用一个计数器变量来记录学生出现在倒数前三的次数。每当学生出现在某门课的倒数前三名时，就将计数器加一。
3️⃣ 最后，你可以打印出每个学生的倒数排名次数。记得将 xxx 替换为计算的结果。
"""

code_ranks = [
  '贾惜春', '贾巧姐', '李纨', '秦可卿', '史湘云', '王熙凤',
  '薛宝钗', '林黛玉', '妙玉', '贾迎春', '贾宝玉', '探春'
]
chinese_ranks = [
  '林黛玉', '薛宝钗', '贾宝玉', '贾探春', '史湘云', '贾迎春',
  '贾惜春', '王熙凤', '贾巧姐', '李纨', '妙玉', '秦可卿'
]
math_ranks = [
  '王熙凤', '贾迎春', '妙玉', '林黛玉', '贾惜春', '贾巧姐',
  '贾探春', '史湘云', '秦可卿', '李纨', '薛宝钗', '贾宝玉'
]


# 定义函数，计算某个学生出现在倒数三名的总次数
def last_three(student_name):
    count = 0  # 计数器
    # 把所有科目的排名表放在一个列表里，方便统一遍历
    all_ranks = [code_ranks, chinese_ranks, math_ranks]
    for ranks in all_ranks:
        if student_name in ranks[-3:]:
            count += 1
    return count


print('贾宝玉排在倒数三名的次数是', last_three("贾宝玉"))
print('秦可卿排在倒数三名的次数是', last_three("秦可卿"))